function t(key) {
  return translations[state.language][key] || translations.en[key] || key;
}

function applyLanguage() {
  document.documentElement.lang = state.language;
  document.documentElement.dir = state.language === "ar" ? "rtl" : "ltr";
  document.body.dir = document.documentElement.dir;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-label]").forEach((label) => {
    const control = label.querySelector("input, textarea");
    const text = t(label.dataset.i18nLabel);
    if (control) {
      label.firstChild.textContent = text;
    }
  });
  document.getElementById("languageToggle").textContent = state.language === "en" ? "ع" : "EN";
  renderProfile();
  renderNetwork();
  renderJobs();
  renderAdmin();
}

function fileSizeLabel(size) {
  if (!size) return "";
  if (size < 1024 * 1024) return `${Math.round(size / 1024)} KB`;
  return `${(size / (1024 * 1024)).toFixed(1)} MB`;
}

function renderTimeline(items, targetId, emptyText) {
  const target = document.getElementById(targetId);
  target.innerHTML = "";
  if (!items.length) {
    target.innerHTML = `<p>${emptyText}</p>`;
    return;
  }
  items.forEach((item) => {
    const title = item.role || item.school;
    const subtitle = item.company || item.degree;
    const node = document.createElement("article");
    node.className = "timeline-item";
    node.innerHTML = `
      <div class="timeline-icon">${title.charAt(0).toUpperCase()}</div>
      <div>
        <h3>${title}</h3>
        <p>${subtitle}</p>
        <small>${item.period}</small>
      </div>
    `;
    target.appendChild(node);
  });
}

function renderBuilderTimeline(items, targetId) {
  renderTimeline(items, targetId, "");
}

function renderFiles(files, targetId, emptyText) {
  const target = document.getElementById(targetId);
  target.innerHTML = "";
  if (!files.length) {
    target.innerHTML = `<p>${emptyText}</p>`;
    return;
  }
  files.forEach((file) => {
    const node = document.createElement("div");
    node.className = "file-item";
    node.innerHTML = `<strong>${file.name}</strong><span>${file.type || "Document"} ${file.size ? "· " + fileSizeLabel(file.size) : ""}</span>`;
    target.appendChild(node);
  });
}

